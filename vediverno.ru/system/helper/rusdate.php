function expiredDateToday($date){
    
   
    $current_day = new DateTime(null, new DateTimeZone('Europe/Moscow'));
    $compare_day = new DateTime($date, new DateTimeZone('Europe/Moscow'));
   
    if ($current_day <= $compare_day) {
        return false;
    }
    return true;
}	


